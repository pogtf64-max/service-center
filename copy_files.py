        # –ö–æ–ø–∏—Ä—É–µ–º —Ñ–∞–π–ª –≤ –∫–æ–Ω—Ç–µ–π–Ω–µ—Ä
        copy_cmd = [
            "ssh",
            "-i", ssh_key,
            ssh_host,
            f"docker cp /tmp/{os.path.basename(local_path)} service-center-service-center-1:{server_path}"
        ]
        
        print(f"üì¶ –ö–æ–ø–∏—Ä—É—é –≤ –∫–æ–Ω—Ç–µ–π–Ω–µ—Ä: {server_path}")
