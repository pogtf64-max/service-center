        if copy_result.returncode == 0:
            print(f"‚úÖ {local_path} ‚Üí {server_path}")
            return True
        else:
            print(f"‚ùå –û—à–∏–±–∫–∞ –∫–æ–ø–∏—Ä–æ–≤–∞–Ω–∏—è –≤ –∫–æ–Ω—Ç–µ–π–Ω–µ—Ä: {copy_result.stderr}")
